namespace Sitecore.SharedSource.Configuration
{
    public class DialogSettings
    {
        /// <summary>
        /// Gets or sets the Ace Code Editor theme.
        /// </summary>
        public string Theme { get; set; }

        /// <summary>
        /// Gets or sets the modal dialog width.
        /// </summary>
        public int Width { get; set; }

        /// <summary>
        /// Gets or sets the modal dialog height.
        /// </summary>
        public int Height { get; set; }

        /// <summary>
        /// Gets or sets the Ace Code Editor font size.
        /// </summary>
        public int FontSize { get; set; }

        /// <summary>
        /// Gets or sets the Ace Code Editor font family.
        /// </summary>
        public string FontFamily { get; set; }
    }
}
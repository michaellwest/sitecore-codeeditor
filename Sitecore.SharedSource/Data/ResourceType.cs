namespace Sitecore.SharedSource.Data
{
    public enum ResourceType
    {
        /// <summary>
        /// Indicates that the resource type is unknown.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// Indicates that the resource is a javascript document.
        /// </summary>
        Script = 1,

        /// <summary>
        /// Indicates that the resource is a stylesheet document.
        /// </summary>
        Style = 2
    }
}